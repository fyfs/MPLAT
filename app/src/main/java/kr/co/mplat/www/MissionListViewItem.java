public class MissionListViewItem {
    private String campaignCode = "";
    private String imgIcon = "";
    private String appTitle = "";
    private String developerName = "";
    private String mission = "";
    private String point = "";
    private String pointRealtimeYn = "";
    private String pointDate = "";
    private String joinYn = "";

    public String getCampaignCode() {
        return campaignCode;
    }

    public void setCampaignCode(String campaignCode) {
        this.campaignCode = campaignCode;
    }

    public String getImgIcon() {
        return imgIcon;
    }

    public void setImgIcon(String imgIcon) {
        this.imgIcon = imgIcon;
    }

    public String getAppTitle() {
        return appTitle;
    }

    public void setAppTitle(String appTitle) {
        this.appTitle = appTitle;
    }

    public String getDeveloperName() {
        return developerName;
    }

    public void setDeveloperName(String developerName) {
        this.developerName = developerName;
    }

    public String getMission() {
        return mission;
    }

    public void setMission(String mission) {
        this.mission = mission;
    }

    public String getPoint() {
        return point;
    }

    public void setPoint(String point) {
        this.point = point;
    }

    public String getPointRealtimeYn() {
        return pointRealtimeYn;
    }

    public void setPointRealtimeYn(String pointRealtimeYn) {
        this.pointRealtimeYn = pointRealtimeYn;
    }

    public String getPointDate() {
        return pointDate;
    }

    public void setPointDate(String pointDate) {
        this.pointDate = pointDate;
    }

    public String getJoinYn() {
        return joinYn;
    }

    public void setJoinYn(String joinYn) {
        this.joinYn = joinYn;
    }
}
